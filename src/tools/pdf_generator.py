# ── Severity badge colours ─────────────────────────────────────────────────────

_SEVERITY_COLOURS: dict[str, str] = {
    "critical": "#c0392b",
    "high":     "#e67e22",
    "medium":   "#f39c12",
    "low":      "#27ae60",
    "info":     "#2980b9",
}

_DEFAULT_SEVERITY_COLOUR = "#7f8c8d"


def _severity_colour(severity: str | None) -> str:
    if not severity:
        return _DEFAULT_SEVERITY_COLOUR
    return _SEVERITY_COLOURS.get(severity.lower(), _DEFAULT_SEVERITY_COLOUR)


# ── Table of contents generation ──────────────────────────────────────────────

def _build_toc(content: str) -> str:
    """Build a simple HTML table of contents from ## headers."""
    toc_entries = []
    for line in content.splitlines():
        m = re.match(r"^(#{1,3})\s+(.+)$", line)
        if m:
            level = len(m.group(1))
            title = m.group(2).strip()
            anchor = re.sub(r"[^a-z0-9-]", "", title.lower().replace(" ", "-"))
            indent = (level - 1) * 16
            toc_entries.append(
                f'<li style="margin-left:{indent}px"><a href="#{anchor}">{title}</a></li>'
            )

    if not toc_entries:
        return ""

    return (
        '<div class="toc">'
        "<h2>Contents</h2>"
        "<ul>" + "".join(toc_entries) + "</ul>"
        "</div>"
    )


def _add_header_anchors(html: str) -> str:
    """Add id anchors to h1/h2/h3 tags for TOC links."""
    def _replace(m: re.Match) -> str:
        tag = m.group(1)
        attrs = m.group(2) or ""
        text = re.sub(r"<[^>]+>", "", m.group(3))
        anchor = re.sub(r"[^a-z0-9-]", "", text.lower().replace(" ", "-"))
        return f"<{tag}{attrs} id=\"{anchor}\">{m.group(3)}</{tag}>"

    return re.sub(r"<(h[1-3])([^>]*)>(.*?)</h[1-3]>", _replace, html, flags=re.DOTALL)


