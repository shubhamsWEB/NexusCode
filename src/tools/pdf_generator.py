def _build_html(
    content_html: str,
    title: str,
    metadata: dict[str, Any],
    toc_html: str,
    now_str: str,
) -> str:
    """Wrap rendered content in a full HTML document with NexusCode styling."""
    severity = metadata.get("severity", "")
    sev_colour = _severity_colour(severity)
    badge_html = (
        f'<span class="severity-badge">{severity.upper()}</span>'
        if severity
        else ""
    )

    # Build metadata table rows
    meta_rows = ""
    meta_display = {
        k: v for k, v in metadata.items()
        if k not in ("severity",) and v
    }
    if meta_display:
        rows = "".join(
            f"<tr><td>{k.replace('_', ' ').title()}</td><td>{v}</td></tr>"
            for k, v in meta_display.items()
        )
        meta_rows = f'<table class="meta-table">{rows}</table>'

    css = _CSS % {"datetime": now_str, "severity_colour": sev_colour}

    return f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<title>{title}</title>
<style>{css}</style>
</head>
<body>
<div class="header">
    <div class="header-brand">NexusCode</div>
    <div class="header-title">{title}</div>
    {badge_html}
</div>
{meta_rows}
{toc_html}
{content_html}
</body>
</html>"""


# ── Public API ─────────────────────────────────────────────────────────────────

def generate_pdf_from_markdown(
    content: str,
    title: str,
    metadata: dict[str, Any] | None = None,
) -> bytes:
    """
    Convert markdown content to PDF bytes.

    Args:
        content:  Full markdown document text.
        title:    Document title shown in the PDF header.
        metadata: Optional key-value pairs (service, severity, environment, author, etc.).

    Returns:
        Raw PDF bytes.
    """
    import markdown as _markdown  # type: ignore[import-untyped]
    import weasyprint

    if metadata is None:
        metadata = {}

    now_str = datetime.now(UTC).strftime("%Y-%m-%d %H:%M UTC")

    # Convert markdown → HTML
    content_html = _markdown.markdown(
        content,
        extensions=["tables", "fenced_code", "codehilite", "nl2br", "sane_lists"],
    )

    # Add anchor IDs to headers so TOC links work
    content_html = _add_header_anchors(content_html)

    # Build table of contents from the original markdown (before HTML conversion)
    toc_html = _build_toc(content)

    full_html = _build_html(
        content_html=content_html,
        title=title,
        metadata=metadata,
        toc_html=toc_html,
        now_str=now_str,
    )

    pdf_bytes: bytes = weasyprint.HTML(string=full_html).write_pdf()
    return pdf_bytes


async def store_document(
    pdf_bytes: bytes,
    title: str,
    filename: str,
    run_id: str | None = None,
    step_id: str | None = None,
    metadata: dict[str, Any] | None = None,
) -> str:
    """
    Insert a generated PDF into the generated_documents table.

    Returns:
        doc_id (UUID string) of the newly created row.
    """
    from sqlalchemy import text

    from src.storage.db import AsyncSessionLocal

    if metadata is None:
        metadata = {}

    import json

    async with AsyncSessionLocal() as session:
        row = (
            await session.execute(
                text("""
                    INSERT INTO generated_documents
                        (run_id, step_id, title, filename, pdf_bytes, size_bytes, metadata)
                    VALUES
                        (:run_id, :step_id, :title, :filename, :pdf_bytes, :size_bytes, CAST(:metadata AS jsonb))
                    RETURNING id
                """),
                {
                    "run_id": run_id,
                    "step_id": step_id,
                    "title": title,
                    "filename": filename,
                    "pdf_bytes": pdf_bytes,
                    "size_bytes": len(pdf_bytes),
                    "metadata": json.dumps(metadata),
                },
            )
        ).mappings().first()
        await session.commit()

    doc_id: str = row["id"]
    logger.info(
        "pdf_generator: stored document doc_id=%s title=%r size=%d run_id=%s",
        doc_id, title, len(pdf_bytes), run_id,
    )
    return doc_id


async def get_documents_for_step(
    run_id: str,
    step_id: str,
) -> list[dict]:
    """
    Return all generated_documents for a specific run+step.

    Used by the workflow executor to attach document metadata to step output.
    """
    from sqlalchemy import text

    from src.storage.db import AsyncSessionLocal

    async with AsyncSessionLocal() as session:
        rows = (
            await session.execute(
                text("""
                    SELECT id, filename, size_bytes, title, created_at
                    FROM generated_documents
                    WHERE run_id = :run_id AND step_id = :step_id
                    ORDER BY created_at
                """),
                {"run_id": run_id, "step_id": step_id},
            )
        ).mappings().all()

    return [dict(r) for r in rows]


def slugify(text: str) -> str:
    """Convert a title to a safe filename slug."""
    slug = text.lower()
    slug = re.sub(r"[^a-z0-9\s-]", "", slug)
    slug = re.sub(r"\s+", "-", slug.strip())
    slug = re.sub(r"-{2,}", "-", slug)
    return slug[:80]
